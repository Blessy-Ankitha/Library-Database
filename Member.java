    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        try {
            pst=con.prepareStatement("update users set username=?, phone=?, email=?, password=?, confirmPassword=?  where userid=?");
            pst.setString(1,txtMembername.getText());
            pst.setString(2,txtMemberphone.getText());
            pst.setString(3,txtMemberemail.getText());
            pst.setString(4,txtMemberpassword.getText());
            pst.setString(5,txtMemberpassword.getText());
            pst.setString(6,txtMemberid.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this,"record updated");
        } catch (SQLException ex) {
            Logger.getLogger(Member.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton6ActionPerformed

