    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        String a=JOptionPane.showInputDialog(this,"Enter name for search");
        try {
            pst=con.prepareStatement("select * from users where username=?");
            pst.setString(1,a);
            rs=pst.executeQuery();
            if(rs.next()){
                txtMemberid.setText(rs.getString(1));
                txtMembername.setText(rs.getString(2));
                txtMemberphone.setText(rs.getString(3));
                txtMemberemail.setText(rs.getString(4));
                txtMemberpassword.setText(rs.getString(5));
            }
            else
            {
                JOptionPane.showMessageDialog(this,"!!No data!!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Member.class.getName()).log(Level.SEVERE, null, ex);

        }
    }//GEN-LAST:event_jButton9ActionPerformed

